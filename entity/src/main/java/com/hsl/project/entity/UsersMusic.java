package com.hsl.project.entity;

public class UsersMusic {
    private Integer musicid;
    private String musicname;
    private String musicstyle;
    private String musiclanguage;
    private String musictime;
    private String musicurl;
    private String musiccover;
    private String username;

    public Integer getMusicid() {
        return musicid;
    }

    public void setMusicid(Integer musicid) {
        this.musicid = musicid;
    }

    public String getMusicname() {
        return musicname;
    }

    public void setMusicname(String musicname) {
        this.musicname = musicname;
    }

    public String getMusicstyle() {
        return musicstyle;
    }

    public void setMusicstyle(String musicstyle) {
        this.musicstyle = musicstyle;
    }

    public String getMusiclanguage() {
        return musiclanguage;
    }

    public void setMusiclanguage(String musiclanguage) {
        this.musiclanguage = musiclanguage;
    }

    public String getMusictime() {
        return musictime;
    }

    public void setMusictime(String musictime) {
        this.musictime = musictime;
    }

    public String getMusicurl() {
        return musicurl;
    }

    public void setMusicurl(String musicurl) {
        this.musicurl = musicurl;
    }

    public String getMusiccover() {
        return musiccover;
    }

    public void setMusiccover(String musiccover) {
        this.musiccover = musiccover;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
